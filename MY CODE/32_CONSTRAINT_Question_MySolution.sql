	CNO VARCHAR2(8) PRIMARY KEY,
	CNAME VARCHAR2(20),
	ST_NUM NUMBER(1, 0),
	PNO VARCHAR2(8)
);

--2) PNO이 PK인 PROFESSOR_PK 테이블을 생성하세요.(2번 방식으로)
CREATE TABLE PROFESSOR_PK(
	PNO VARCHAR2(8),
	PNAME VARCHAR2(20),
	SECTION VARCHAR2(20),
	ORDERS VARCHAR2(10),
	HIREDATE DATE,
	CONSTRAINT PK_PROFESSOR_PK_PNO PRIMARY KEY(PNO)
);

