INSERT INTO EMP(ENO, ENAME, JOB, HDATE) VALUES ('9993', '정도전', '경영', SYSDATE);
-- COMMIT이 발생한 작업에 대해서는 ROLLBACK으로 취소할 수 없다.
ROLLBACK;

-- INSERT INTO 시  지정한 컬럼의 개수와 컬럼의 타입에 맞는 데이터를 입력해야 한다.
